package academy.devdojo.variaveis;

public class ReutilizandoVariaveis {
    public static void main(String[] args) {
        double salario = 5000;
        double resultado = salario * 0.4;
        System.out.println("40% eh: " + resultado);
        resultado = salario * 0.30;
        System.out.println("30% eh: " + resultado);
        resultado = salario * 0.15;
        System.out.println("15% eh: " + resultado);
        resultado = salario * 0.10;
        System.out.println("10% eh: " + resultado);
        resultado = salario * 0.05;
        System.out.println("5% eh: " + resultado);

    }
}

